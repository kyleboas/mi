import assert from 'node:assert/strict';
import { mkdtemp, mkdir, readFile, rm, writeFile } from 'node:fs/promises';
import { tmpdir } from 'node:os';
import { join } from 'node:path';
import { execFile } from 'node:child_process';
import { promisify } from 'node:util';
import { generateProjectsStatus, parsePlanReadme, parseProjectsIndex, renderProjectsStatus } from '../dist/src/project-status.js';

const execFileAsync = promisify(execFile);
const root = await mkdtemp(join(tmpdir(), 'mi-project-status-'));
try {
  const repo = join(root, 'repo');
  await mkdir(repo, { recursive: true });
  await execFileAsync('git', ['init'], { cwd: repo });
  await execFileAsync('git', ['checkout', '-b', 'main'], { cwd: repo });
  await writeFile(join(repo, 'README.md'), '# repo\n');
  await execFileAsync('git', ['add', 'README.md'], { cwd: repo });
  await execFileAsync('git', ['-c', 'user.email=mi@example.invalid', '-c', 'user.name=Mi Test', 'commit', '-m', 'init'], { cwd: repo });

  const projectsPath = join(root, 'PROJECTS.md');
  const plansPath = join(root, 'plans-README.md');
  const outputPath = join(root, 'projects-status.md');
  const projectsMarkdown = `# Projects\n\n| Repo | Local Path | Purpose |\n|---|---|---|\n| [kyleboas/example](https://github.com/kyleboas/example) | \`${repo}\` | Example repo |\n| [kyleboas/no-local](https://github.com/kyleboas/no-local) | — | Remote only |\n`;
  await writeFile(projectsPath, projectsMarkdown);
  await writeFile(plansPath, `# Plans\n\n| Plan | Status |\n|---|---|\n| kyleboas/example launch | In progress |\n`);

  const parsed = parseProjectsIndex(projectsMarkdown);
  assert.equal(parsed.length, 2);
  assert.equal(parsed[0].repo, 'kyleboas/example');
  assert.equal(parsed[0].localPath, repo);
  assert.equal(parsePlanReadme(await readFile(plansPath, 'utf8')).get('kyleboas/example'), 'kyleboas/example launch - In progress');

  const result = await generateProjectsStatus({ projectsPath, plansReadmePath: plansPath, outputPath });
  assert.equal(result.projects, 2);
  const generated = await readFile(outputPath, 'utf8');
  assert.match(generated, /^# Projects Status/);
  assert.match(generated, /generated by Mi; do not hand-edit/);
  assert.match(generated, /kyleboas\/example[\s\S]*main[\s\S]*kyleboas\/example launch - In progress/);
  assert.match(generated, /kyleboas\/no-local[\s\S]*—/);
  assert.equal(renderProjectsStatus([], new Date('2026-01-02T03:04:05Z')).includes('2026-01-02T03:04:05.000Z'), true);
} finally {
  await rm(root, { recursive: true, force: true });
}

console.log('Mi project status checks passed.');
